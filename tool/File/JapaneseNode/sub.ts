import { コンソール, 変数ファクトリ, 配列作成, 配列各要素, 文字列結合, 遅延実行 } from ".";


コンソール.ログ("--- 日本語風Node.js 簡単サンプル　---");

const カウント = 変数ファクトリ(0); 
コンソール.ログ("最初のカウント:", カウント.値); 

カウント.値 = カウント.値 + 5; 
コンソール.ログ("増やした後のカウント:", カウント.値);

const 果物リスト = 配列作成("りんご", "ばなな", "みかん");
コンソール.ログ("作った配列:", 果物リスト);

コンソール.ログ("リストの各果物:");
配列各要素(果物リスト, (果物, 番号) => {
    コンソール.ログ(`  ${番号 + 1}番目: ${果物}`);
});

const メッセージ部品 = 配列作成("こんにちは", "世界", "！");
const 結合メッセージ = 文字列結合(メッセージ部品, " "); 
コンソール.ログ("結合したメッセージ:", 結合メッセージ);

コンソール.ログ("1秒待ってから、最後のメッセージを出します...");
遅延実行(1000, () => {
    コンソール.情報("...おしまい！");
});

コンソール.ログ("--- サンプル処理 完了 ---");